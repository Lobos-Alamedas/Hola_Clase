console.log("Ciclo for");       // Titulo.

for(var i=2;i<=100;i=i+1){      // Incio de for.

    if(i==55){

        console.log("Este numero es 55");

    }
    if(i==100){

        console.log("Este numero es 100");
    }
    else{

        console.log("No es 55 ni 100");
    }


}                               // Fin de for.
